//concat () Sirve para unir los datos de dos array en una nueva variable

const colores1 = ['rojo', 'azul', 'verde'];
const colores2 = ['red', 'blue', 'green'];

//creamos la variable donde se guarda la unión
const nuevoarray = colores1.concat(colores2);

console.log(nuevoarray);
//el resultado es mostrar en un nuevo array [ 'rojo', 'azul', 'verde', 'red', 'blue', 'green' ]
